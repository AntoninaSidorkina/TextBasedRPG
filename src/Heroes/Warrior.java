package Heroes;

public class Warrior {
    String heroesName = "Warrior";
    int heroesDefaultHp = 200;
    int heroesDefaultDMG = 10;
    int heroesDefaultArmor = 10;
    int heroesDefaultMdamage = 5;
    int heroesDefaultMarmor = 3;


    public String getHeroesName() {
        return heroesName;
    }

    public void setHeroesName(String heroesName) {
        this.heroesName = heroesName;
    }

    public int getHeroesDefaultHp() {
        return heroesDefaultHp;
    }

    public void setHeroesDefaultHp(int heroesDefaultHp) {
        this.heroesDefaultHp = heroesDefaultHp;
    }

    public int getHeroesDefaultDMG() {
        return heroesDefaultDMG;
    }

    public void setHeroesDefaultDMG(int heroesDefaultDMG) {
        this.heroesDefaultDMG = heroesDefaultDMG;
    }

    public int getHeroesDefaultArmor() {
        return heroesDefaultArmor;
    }

    public void setHeroesDefaultArmor(int heroesDefaultArmor) {
        this.heroesDefaultArmor = heroesDefaultArmor;
    }

    public int getHeroesDefaultMdamage() {
        return heroesDefaultMdamage;
    }

    public void setHeroesDefaultMdamage(int heroesDefaultMdamage) {
        this.heroesDefaultMdamage = heroesDefaultMdamage;
    }

    public int getHeroesDefaultMarmor() {
        return heroesDefaultMarmor;
    }

    public void setHeroesDefaultMarmor(int heroesDefaultMarmor) {
        this.heroesDefaultMarmor = heroesDefaultMarmor;
    }

    public Warrior() {
        this.heroesName = heroesName;
        this.heroesDefaultHp = heroesDefaultHp;
        this.heroesDefaultDMG = heroesDefaultDMG;
        this.heroesDefaultArmor = heroesDefaultArmor;
        this.heroesDefaultMdamage = heroesDefaultMdamage;
        this.heroesDefaultMarmor = heroesDefaultMarmor;
    }
}
